#include <conio.h>
#include <string.h>

#define POKE(addr, val) (*(unsigned char *)(addr) = (unsigned char)(val))

static const unsigned char sprite_data[63] = {
    0x00,0x7e,0x00,
    0x01,0xff,0x80,
    0x03,0xff,0xc0,
    0x07,0xdb,0xe0,
    0x07,0xff,0xe0,
    0x07,0x7e,0xe0,
    0x07,0xff,0xe0,
    0x03,0xff,0xc0,
    0x01,0xff,0x80,
    0x00,0x7e,0x00,
    0x00,0x3c,0x00,
    0x00,0x18,0x00,
    0x00,0x3c,0x00,
    0x00,0x66,0x00,
    0x00,0xc3,0x00,
    0x01,0x81,0x80,
    0x03,0x00,0xc0,
    0x06,0x00,0x60,
    0x0c,0x00,0x30,
    0x18,0x00,0x18,
    0x00,0x00,0x00
};

int main(void) {
    unsigned char *sprite_memory = (unsigned char *)0x0340;

    clrscr();
    bordercolor(COLOR_GREEN);
    bgcolor(COLOR_BLACK);
    textcolor(COLOR_LIGHTGREEN);

    memcpy(sprite_memory, sprite_data, sizeof(sprite_data));

    POKE(0x07f8, 13);       /* sprite 0 pointer: 13 * 64 = $0340 */
    POKE(0xd000, 160);      /* sprite 0 X */
    POKE(0xd001, 120);      /* sprite 0 Y */
    POKE(0xd015, 1);        /* enable sprite 0 */
    POKE(0xd027, COLOR_YELLOW);

    cputsxy(7, 3,  "C64 LEARNING LAB");
    cputsxy(8, 5,  "LAB 006 SPRITE BASICS");
    cputsxy(5, 20, "SPRITE 0 DATA AT $0340");
    cputsxy(5, 22, "PRESS ANY KEY TO END");

    cgetc();
    POKE(0xd015, 0);
    return 0;
}
